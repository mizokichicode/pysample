def add(a, b):
    '''calculate (a + b)
    '''
    return (a + b)


def subtraction(a, b):
    '''calculate (a - b)
    '''
    return (a - b)


def multiplication(a, b):
    '''calculate (a * b)
    '''
    return (a * b)


def division(a, b):
    '''calculate (a // b)
    '''
    if a > 0:
        return (a // b)
    return 0
    